/**
 * cc_types.h — 基础类型定义模块
 *
 * @file    cc/core/cc_types.h
 * @brief   定义 c-claw 项目中最基础的类型别名、宏常量和跨平台兼容层。
 *
 * 本模块是整个项目类型系统的根基。其他所有头文件（cc_result.h、
 * cc_message.h、cc_memory_entry.h 等）都在此模块之上构建。
 * 它承担以下职责：
 *
 * 1. 提供跨平台的基础类型抽象（避免直接使用 int、long 等平台相关类型）
 * 2. 定义项目级别的布尔类型、空指针哨兵等通用常量
 * 3. 引入标准库中最基础的定义（如 size_t），减少各模块的重复引入
 *
 * ─── 接口契约 ─────────────────────────────────────────────────────────
 *
 *   本模块仅包含类型定义和预处理器宏，不包含任何函数或业务逻辑。
 *   它不依赖项目中任何其他模块，是最底层的模块。
 *   所有其他 cc_* 模块都直接或间接地依赖本模块。
 *
 * ─── 设计原则 ─────────────────────────────────────────────────────────
 *
 *   - 零依赖：仅依赖 C 标准库，不依赖任何项目内部模块
 *   - 极简：只放真正跨模块共享的类型，避免成为"大杂烩"
 *   - 渐进扩展：随着项目演进，将反复出现的类型抽象提升到此模块
 *
 * ─── 依赖 ─────────────────────────────────────────────────────────────
 *
 *   仅依赖 <stddef.h>（提供 size_t、NULL 等）。
 *   不依赖任何 cc_* 模块，被项目中几乎所有模块间接依赖。
 */

#ifndef CC_TYPES_H
#define CC_TYPES_H

#include <stddef.h>

#endif